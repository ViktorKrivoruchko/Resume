-- Запросы к схема_аэропорта.sql
SELECT Дата, ФИО, Город
FROM Билет
join Пассажир on idПассажир = Пассажир
join Аэропорт on idАэропорт = Аэропорт
where (ФИО like 'Иванов%' or ФИО like 'Петров%') and (Город = 'Самара' or Город = 'Калининград') and (extract(month from Дата) = 05 or extract(month from Дата) = 04)


