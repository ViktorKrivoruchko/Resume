SELECT  Дата, Название, `Регистрационный номер`
FROM Заявитель join Заявление on idЗаявитель = Человек
join Город on idГород = `Адрес жительства`
where Название = "Барнаул" and Дата > '2023-04-23' and `Регистрационный номер`%2=0;

